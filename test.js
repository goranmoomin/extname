const test = require("ava").default;
const extname = require("./");

test("path: __filename", t => t.is(extname(__filename), ".js"));
test("path: ", t => t.is(extname(""), ""));
test("path: /path/to/file", t => t.is(extname("/path/to/file"), ""));
test("path: /path/to/file.ext", t => t.is(extname("/path/to/file.ext"), ".ext"));
test("path: /path.to/file.ext", t => t.is(extname("/path.to/file.ext"), ".ext"));
test("path: /path.to/file", t => t.is(extname("/path.to/file"), ""));
test("path: /path.to/.file", t => t.is(extname("/path.to/.file"), ""));
test("path: /path.to/.file.ext", t => t.is(extname("/path.to/.file.ext"), ".ext"));
test("path: /path/to/f.ext", t => t.is(extname("/path/to/f.ext"), ".ext"));
test("path: /path/to/..ext", t => t.is(extname("/path/to/..ext"), ".ext"));
test("path: /path/to/..", t => t.is(extname("/path/to/.."), ""));
test("path: file", t => t.is(extname("file"), ""));
test("path: file.ext", t => t.is(extname("file.ext"), ".ext"));
test("path: .file", t => t.is(extname(".file"), ""));
test("path: .file.ext", t => t.is(extname(".file.ext"), ".ext"));
test("path: /file", t => t.is(extname("/file"), ""));
test("path: /file.ext", t => t.is(extname("/file.ext"), ".ext"));
test("path: /.file", t => t.is(extname("/.file"), ""));
test("path: /.file.ext", t => t.is(extname("/.file.ext"), ".ext"));
test("path: .path/file.ext", t => t.is(extname(".path/file.ext"), ".ext"));
test("path: file.ext.ext", t => t.is(extname("file.ext.ext"), ".ext"));
test("path: file.", t => t.is(extname("file."), "."));
test("path: .", t => t.is(extname("."), ""));
test("path: ./", t => t.is(extname("./"), ""));
test("path: .file.", t => t.is(extname(".file."), "."));
test("path: .file..", t => t.is(extname(".file.."), "."));
test("path: ..", t => t.is(extname(".."), ""));
test("path: ../", t => t.is(extname("../"), ""));
test("path: ..file.ext", t => t.is(extname("..file.ext"), ".ext"));
test("path: ..file", t => t.is(extname("..file"), ".file"));
test("path: ..file.", t => t.is(extname("..file."), "."));
test("path: ..file..", t => t.is(extname("..file.."), "."));
test("path: ...", t => t.is(extname("..."), "."));
test("path: ...ext", t => t.is(extname("...ext"), ".ext"));
test("path: ....", t => t.is(extname("...."), "."));
test("path: file.ext/", t => t.is(extname("file.ext/"), ".ext"));
test("path: file.ext//", t => t.is(extname("file.ext//"), ".ext"));
test("path: file/", t => t.is(extname("file/"), ""));
test("path: file//", t => t.is(extname("file//"), ""));
test("path: file./", t => t.is(extname("file./"), "."));
test("path: file.//", t => t.is(extname("file.//"), "."));
test("path: .\\", t => t.is(extname(".\\"), ""));
test("path: ..\\", t => t.is(extname("..\\"), ".\\"));
test("path: file.ext\\", t => t.is(extname("file.ext\\"), ".ext\\"));
test("path: file.ext\\\\", t => t.is(extname("file.ext\\\\"), ".ext\\\\"));
test("path: file\\", t => t.is(extname("file\\"), ""));
test("path: file\\\\", t => t.is(extname("file\\\\"), ""));
test("path: file.\\", t => t.is(extname("file.\\"), ".\\"));
test("path: file.\\\\", t => t.is(extname("file.\\\\"), ".\\\\"));
